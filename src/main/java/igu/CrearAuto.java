    public void MostrarMensaje(String mensaje, String tipo, String titulo) {

        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);

    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //guardar datos desde el igu

        String modelo = txtmodelo.getText();
        String marca = txtmarca.getText();
        String motor = txtmotor.getText();
        String color = txtcolor.getText();
        int patente = Integer.parseInt(txtpatente.getText());
        String cantidadPuerta = (String) cmbcantidadPuertas.getSelectedItem();

        controladora.guardar(modelo, marca, motor, color, patente, cantidadPuerta);

        MostrarMensaje("se guardo correctamente", "info", "guardado correcto");
        
        btnLimpiarActionPerformed(evt);
    }//GEN-LAST:event_btnGuardarActionPerformed


